export const sumar = (number1: number, number2: number): number => number1 + number2;
export const restar = (number1: number, number2: number): number => number1 - number2;
export const multiplicar = (number1: number, number2: number): number => number1 * number2;
export const dividir = (number1: number, number2: number): number => number1 / number2;
